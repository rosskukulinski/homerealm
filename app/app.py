def opts(values, current):
    return "".join(f'<option{" selected" if v == str(current) else ""}>{v}</option>'
                   for v in values)

def players_html(name):
    roster, perms = seen_players(name), permissions(name)
    if not roster:
        return '<p class="meta">No one has joined yet — players appear here after their first visit.</p>'
    rows = ""
    for xuid, tag in sorted(roster.items(), key=lambda kv: kv[1].lower()):
        level = perms.get(xuid, "member")
        rows += (f'<tr><td><b>{tag}</b></td><td>'
                 f'<form class="inline" method="post" action="/permission/{name}">'
                 f'<input type="hidden" name="xuid" value="{xuid}">'
                 f'<select name="level">{opts(PERMS, level)}</select> '
                 f'<button class="neutral">Set</button></form></td></tr>')
    return f"<table>{rows}</table>"

@app.get("/")
def index():
    blocks = ""
    for w in load():
        st = status(w["name"])
        ss = (f'<form class="inline" method="post" action="/stop/{w["name"]}"><button class="neutral">Stop</button></form>'
              if st == "running" else
              f'<form class="inline" method="post" action="/start/{w["name"]}"><button class="go">Start</button></form>')
        addr = f'connect: <span class="addr">{REMOTE_ADDRESS or "&lt;host&gt;"}:{w["port"]}</span>'
        if DISCOVERY and w.get("ip"):
            addr = "home: auto (LAN Games) &middot; away: " + addr.replace("connect: ", "")
        blocks += WORLD_TPL.format(
            name=w["name"], mode=w["mode"], difficulty=w["difficulty"], status=st,
            addr_line=addr, startstop=ss,
            mode_opts=opts(MODES, w["mode"]), diff_opts=opts(DIFFS, w["difficulty"]),
            cheat_opts=opts(["false", "true"], str(w["cheats"]).lower()),
            perm_opts=opts(PERMS, w["default_permission"]),
            max_players=w["max_players"], view_distance=w["view_distance"],
            players=players_html(w["name"]))
    preset_opts = "".join(f'<option value="{k}">{v["label"]}</option>'
                          for k, v in PRESETS.items())
    note = ("Running worlds appear automatically under Friends &rarr; LAN Games "
            "on devices in the same network." if DISCOVERY else
            "Add worlds on devices via Servers &rarr; Add Server with the address shown.")
    return Response(PAGE.format(worlds=blocks or "<p>No worlds yet — make one below!</p>",
                                preset_opts=preset_opts, discovery_note=note,
                                version=__version__), mimetype="text/html")

@app.post("/new")
def new():
    ws = load()
    name = request.form["name"].strip().lower()
    preset = PRESETS.get(request.form.get("preset", ""))
    mode = preset["mode"] if preset else request.form.get("mode", "survival")
    if not re.fullmatch(r"[a-z0-9-]{1,20}", name) or mode not in MODES:
        return "bad name/mode", 400
    if any(w["name"] == name for w in ws):
        return "world exists", 409
    port, ip = alloc(ws)
    if port is None:
        return "no free world IPs — raise WORLD_IP_LAST", 507
    w = {"name": name, "port": port, "mode": mode, "ip": ip, **DEFAULTS,
         "seed": request.form.get("seed", "").strip(),
         "level_type": "FLAT" if request.form.get("flat") else "DEFAULT"}
    if preset:
        w.update(difficulty=preset["difficulty"], cheats=preset["cheats"])
    elif mode == "creative":
        w.update(difficulty="peaceful", cheats=True)
    d = DATA / name
    d.mkdir(exist_ok=True)
    own(d)
    save(ws + [w]); regen(); compose("up", "-d", f"mc-{name}")
    return redirect("/")

@app.post("/settings/<name>")
def settings(name):
    ws = load()
    for w in ws:
        if w["name"] == name:
            mode = request.form.get("mode", w["mode"])
            diff = request.form.get("difficulty", w["difficulty"])
            perm = request.form.get("default_permission", w["default_permission"])
            if mode not in MODES or diff not in DIFFS or perm not in PERMS:
                return "bad value", 400
            w.update(mode=mode, difficulty=diff, default_permission=perm,
                     cheats=request.form.get("cheats") == "true",
                     max_players=max(1, min(30, int(request.form.get("max_players", 10)))),
                     view_distance=max(5, min(32, int(request.form.get("view_distance", 10)))))
            save(ws); regen(); compose("up", "-d", f"mc-{name}")
            return redirect("/")
    return "no such world", 404

@app.post("/permission/<name>")
def permission(name):
    if not any(w["name"] == name for w in load()):
        return "no such world", 404
    xuid, level = request.form["xuid"], request.form["level"]
    if not re.fullmatch(r"\d{5,20}", xuid) or level not in PERMS:
        return "bad value", 400
    set_permission(name, xuid, level)
    subprocess.run(["docker", "restart", f"mc-{name}"], capture_output=True)
    return redirect("/")

@app.post("/clone/<name>")
def clone(name):
    import shutil
    ws = load()
    src = next((w for w in ws if w["name"] == name), None)
    if not src:
        return "no such world", 404
    newname = request.form.get("newname", "").strip().lower()
    if not re.fullmatch(r"[a-z0-9-]{1,20}", newname):
        return "bad name", 400
    if any(w["name"] == newname for w in ws):
        return "world exists", 409
    port, ip = alloc(ws)
    if port is None:
        return "no free world IPs — raise WORLD_IP_LAST", 507
    was_running = status(name) == "running"
    if was_running:                      # cold copy: leveldb hates live copies
        subprocess.run(["docker", "stop", f"mc-{name}"], capture_output=True)
    shutil.copytree(DATA / name, DATA / newname)
    old_world = DATA / newname / "worlds" / name
    if old_world.exists():
        old_world.rename(DATA / newname / "worlds" / newname)
    own(DATA / newname)
    if was_running:
        subprocess.run(["docker", "start", f"mc-{name}"], capture_output=True)
    w = {**src, "name": newname, "port": port, "ip": ip}
    save(ws + [w]); regen(); compose("up", "-d", f"mc-{newname}")
    return redirect("/")

@app.post("/delete/<name>")
def delete(name):
    ws = load()
    keep = [w for w in ws if w["name"] != name]
    if len(keep) == len(ws):
        return "no such world", 404
    compose("rm", "-sf", f"mc-{name}")
    arch = DATA / "_archive"
    arch.mkdir(exist_ok=True)
    (DATA / name).rename(arch / f"{name}-{datetime.datetime.now():%Y%m%d-%H%M}")
    save(keep); regen()
    return redirect("/")

@app.post("/start/<name>")
def start(name):
    subprocess.run(["docker", "start", f"mc-{name}"], capture_output=True)
    return redirect("/")

@app.post("/stop/<name>")
def stop(name):
    subprocess.run(["docker", "stop", f"mc-{name}"], capture_output=True)
    return redirect("/")

@app.post("/restart/<name>")
def restart(name):
    subprocess.run(["docker", "restart", f"mc-{name}"], capture_output=True)
    return redirect("/")

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=PANEL_PORT)
