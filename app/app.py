#!/usr/bin/env python3
"""homerealm — a self-hosted, Realm-style panel for Minecraft Bedrock worlds.

Runs each world as its own always-on container (itzg/minecraft-bedrock-server),
managed from a small web panel: create (presets/seed/flat), clone, settings,
player permissions, start/stop, delete-to-archive.

Configuration is via environment variables (see README / .env.example).
No auth by design — expose only on your LAN or a VPN (e.g. Tailscale).
"""
import datetime, json, os, re, subprocess
from pathlib import Path
from flask import Flask, request, redirect, Response, jsonify

__version__ = "0.1.0"

# ---------- configuration ----------
DATA = Path(os.environ.get("DATA_DIR", "/data"))          # inside this container
HOST_DATA = os.environ.get("HOST_DATA_DIR", "/volume1/docker/homerealm")
PUID = os.environ.get("PUID", "1000")
PGID = os.environ.get("PGID", "100")
PANEL_PORT = int(os.environ.get("PANEL_PORT", "8090"))
BASE_PORT = int(os.environ.get("BASE_PORT", "19132"))     # first world port
REMOTE_ADDRESS = os.environ.get("REMOTE_ADDRESS", "")     # shown as "away" address
BEDROCK_IMAGE = os.environ.get("BEDROCK_IMAGE", "itzg/minecraft-bedrock-server:latest")
PROJECT = "homerealm-worlds"                               # compose project name

# Optional LAN auto-discovery (macvlan): each world gets its own LAN IP on the
# default Bedrock port so consoles/tablets list it under "LAN Games".
DISCOVERY = os.environ.get("DISCOVERY_ENABLED", "false").lower() == "true"
MACVLAN_NETWORK = os.environ.get("MACVLAN_NETWORK", "mcnet")
WORLD_IP_PREFIX = os.environ.get("WORLD_IP_PREFIX", "192.168.1.")
WORLD_IP_FIRST = int(os.environ.get("WORLD_IP_FIRST", "250"))
WORLD_IP_LAST = int(os.environ.get("WORLD_IP_LAST", "254"))

WORLDS = DATA / "worlds.json"
COMPOSE = DATA / "docker-compose.yml"
DIFFS = ["peaceful", "easy", "normal", "hard"]
MODES = ["survival", "creative", "adventure"]
PERMS = ["visitor", "member", "operator"]
PRESETS = {
    "survival-easy":     {"label": "Survival (easy)",   "mode": "survival",  "difficulty": "easy",     "cheats": False},
    "survival-peaceful": {"label": "Peaceful survival", "mode": "survival",  "difficulty": "peaceful", "cheats": False},
    "survival-hard":     {"label": "Hard survival",     "mode": "survival",  "difficulty": "hard",     "cheats": False},
    "creative":          {"label": "Creative sandbox",  "mode": "creative",  "difficulty": "peaceful", "cheats": True},
    "adventure":         {"label": "Adventure",         "mode": "adventure", "difficulty": "normal",   "cheats": False},
}
DEFAULTS = {"difficulty": "easy", "cheats": False, "max_players": 10,
            "view_distance": 10, "seed": "", "level_type": "DEFAULT",
            "default_permission": "member"}

app = Flask(__name__)

# ---------- model ----------
def load():
    return json.loads(WORLDS.read_text()) if WORLDS.exists() else []

def save(ws):
    WORLDS.write_text(json.dumps(ws, indent=1))

def status(name):
    r = subprocess.run(["docker", "inspect", "-f", "{{.State.Status}}", f"mc-{name}"],
                       capture_output=True, text=True)
    return r.stdout.strip() if r.returncode == 0 else "stopped"

def regen():
    lines = ["# GENERATED by homerealm — edit via the panel or CLI, not by hand.",
             "services:"]
    for w in load():
        env = {"EULA": "TRUE", "VERSION": "LATEST", "SERVER_NAME": w["name"],
               "GAMEMODE": w["mode"], "DIFFICULTY": w["difficulty"],
               "LEVEL_NAME": w["name"], "ALLOW_CHEATS": str(w["cheats"]).lower(),
               "ALLOW_LIST": "false", "MAX_PLAYERS": w["max_players"],
               "VIEW_DISTANCE": w["view_distance"],
               "DEFAULT_PLAYER_PERMISSION_LEVEL": w["default_permission"]}
        if w.get("seed"):
            env["LEVEL_SEED"] = w["seed"]
        if w.get("level_type", "DEFAULT") != "DEFAULT":
            env["LEVEL_TYPE"] = w["level_type"]
        envs = ", ".join(f'{k}: "{v}"' for k, v in env.items())
        net = (f'\n    networks: {{ default: {{}}, {MACVLAN_NETWORK}: '
               f'{{ ipv4_address: "{w["ip"]}" }} }}') if DISCOVERY and w.get("ip") else ""
        lines += [f'''  mc-{w["name"]}:
    image: {BEDROCK_IMAGE}
    container_name: mc-{w["name"]}
    restart: unless-stopped
    environment: {{ {envs} }}
    ports: ["{w["port"]}:19132/udp"]{net}
    volumes: ["{HOST_DATA}/{w["name"]}:/data"]''']
    if DISCOVERY:
        lines += ["networks:", f"  {MACVLAN_NETWORK}: {{ external: true }}"]
    COMPOSE.write_text("\n".join(lines) + "\n")

def compose(*args):
    r = subprocess.run(["docker", "compose", "-p", PROJECT, "-f", str(COMPOSE)]
                       + list(args), capture_output=True, text=True)
    if r.returncode != 0:
        print(f"compose {args} failed: {r.stderr[-500:]}", flush=True)
    return r

def alloc(ws):
    """Next free port and (if discovery) LAN IP."""
    port = max([w["port"] for w in ws] + [BASE_PORT - 1]) + 1
    ip = None
    if DISCOVERY:
        used = [int(w["ip"].rsplit(".", 1)[1]) for w in ws if w.get("ip")]
        octet = max(used + [WORLD_IP_FIRST - 1]) + 1
        if octet > WORLD_IP_LAST:
            return None, None
        ip = f"{WORLD_IP_PREFIX}{octet}"
    return port, ip

def own(path):
    subprocess.run(["chown", "-R", f"{PUID}:{PGID}", str(path)])
    subprocess.run(["chmod", "-R", "u+rwX,g+rwX", str(path)])

def seen_players(name):
    """Merge players from container logs into a persistent per-world roster."""
    roster_f = DATA / name / "players_seen.json"
    roster = json.loads(roster_f.read_text()) if roster_f.exists() else {}
    logs = subprocess.run(["docker", "logs", f"mc-{name}"],
                          capture_output=True, text=True).stdout
    for m in re.finditer(r"Player connected: ([^,]+), xuid: (\d+)", logs):
        roster[m.group(2)] = m.group(1).strip()
    try:
        roster_f.write_text(json.dumps(roster))
    except OSError:
        pass
    return roster

def permissions(name):
    f = DATA / name / "permissions.json"
    try:
        return {e["xuid"]: e["permission"] for e in json.loads(f.read_text())}
    except Exception:
        return {}

def set_permission(name, xuid, level):
    f = DATA / name / "permissions.json"
    perms = permissions(name)
    if level == "member":
        perms.pop(xuid, None)
    else:
        perms[xuid] = level
    f.write_text(json.dumps([{"permission": v, "xuid": k} for k, v in perms.items()],
                            indent=1))

